function getEmbedURL(url){

    // 🔵 GOOGLE DRIVE
    if(url.includes("drive.google.com")){
        const match = url.match(/\/d\/(.*?)\//);
        if(match){
            return {
                type:"iframe",
                src:`https://drive.google.com/file/d/${match[1]}/preview`
            };
        }
    }

    // 🔴 YOUTUBE
    if(url.includes("youtube.com") || url.includes("youtu.be")){
        const match = url.match(/(v=|youtu\.be\/)([^&]+)/);
        if(match){
            return {
                type:"iframe",
                src:`https://www.youtube.com/embed/${match[2]}`
            };
        }
    }

    // 🟢 MP4 DIRECTO
    if(url.endsWith(".mp4")){
        return {
            type:"video",
            src:url
        };
    }

    // 🟣 MEGA (solo embed)
    if(url.includes("mega.nz/embed")){
        return {
            type:"iframe",
            src:url
        };
    }

    // ❌ TODO LO DEMÁS
    return null;
}

function openVideoPlayer(videoSrc){

    if(videoSrc.includes("mega.nz") && !videoSrc.includes("embed")){
        showMegaModal(videoSrc);
        return;
    }

    // 🔥 ELIMINA VIDEO ANTERIOR
    const oldPlayer = document.querySelector('.video-player');
    if(oldPlayer) oldPlayer.remove();

    const videoData = getEmbedURL(videoSrc);

    if(!videoData){
        alert("❌ Este servidor no es compatible");
        return;
    }

    const videoPlayer = document.createElement('div');
    videoPlayer.classList.add('video-player');

    let content = "";

    if(videoData.type === "video"){
        content = `
        <video id="videoPlayer"
            controls
            controlsList="nodownload noplaybackrate noremoteplayback"
            disablePictureInPicture
            autoplay
            preload="metadata"
            width="100%">
            <source src="${videoData.src}" type="video/mp4">
        </video>`;
    }

    if(videoData.type === "iframe"){
        content = `
        <iframe src="${videoData.src}" allow="autoplay" allowfullscreen></iframe>`;
    }

    videoPlayer.innerHTML = content;
    document.body.appendChild(videoPlayer);

    const video = document.getElementById("videoPlayer");

    if(video){
        // 🚫 Bloquear clic derecho
        video.addEventListener("contextmenu", e => e.preventDefault());
    }
}

document.addEventListener('click', function(event) {
    if (event.target.classList.contains('video-player')) {
        const video = event.target.querySelector('video');
        if(video){
            video.pause();
            video.src = "";
        }
        event.target.remove();
    }
});

// Cerrar con la tecla Escape
document.addEventListener('keydown', function(event) {
    if (event.key === 'Escape') {
        const videoPlayer = document.querySelector('.video-player');
        if (videoPlayer) {
            videoPlayer.remove();
        }
    }
});

function showMegaModal(url){

    const modal = document.createElement('div');
    modal.classList.add('mega-modal');

    modal.innerHTML = `
        <div class="mega-box">
            <h2>⚠️ Servidor externo</h2>
            <p>Este video se abrirá en una nueva pestaña</p>
            <div class="mega-buttons">
                <button id="openMega">🚀 Abrir en MEGA</button>
                <button id="cancelMega">Cancelar</button>
            </div>
        </div>
    `;

    document.body.appendChild(modal);

    document.getElementById('openMega').onclick = () => {
        window.open(url, "_blank");
        modal.remove();
    };

    document.getElementById('cancelMega').onclick = () => {
        modal.remove();
    };
}
